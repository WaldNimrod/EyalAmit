/**
 * Return the full content map for a single W2-05 product page.
 *
 * @param string $slug One of: didgeridoos, bags, stands-storage, stand-floor, repair.
 * @return array<int,array<string,mixed>>|null
 */
function ea_w2_05_page_content( $slug ) {
	$pages = ea_w2_05_all_page_content();
	return isset( $pages[ $slug ] ) ? $pages[ $slug ] : null;
}

